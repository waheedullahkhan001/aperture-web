export function icon(name, cls = 'size-4') {
  const body = ICONS[name];
  if (!body) return '';
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor"`
    + ` stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="${cls}" aria-hidden="true">${body}</svg>`;
}

// Declarative icons for STATIC markup: any element with data-icon="name" gets the
// SVG prepended once (data-icon-size overrides the default). Lets plain HTML buttons
// use the same icon set as JS-rendered content without per-page wiring. Auto-runs on
// load (module scripts are deferred, so the DOM is parsed by the time this executes).
export function decorateIcons(root = document) {
  root.querySelectorAll('[data-icon]:not([data-icon-done])').forEach((el) => {
    el.dataset.iconDone = '1';
    el.insertAdjacentHTML('afterbegin', icon(el.dataset.icon, el.dataset.iconSize || 'size-4'));
    if (el.tagName === 'BUTTON' || el.tagName === 'A') el.classList.add('gap-2');
  });
}

if (typeof document !== 'undefined') {
  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', () => decorateIcons());
  else decorateIcons();
}
